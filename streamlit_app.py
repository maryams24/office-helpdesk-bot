def _to_wav_bytes(sr: int, samples_f32: list[float]) -> bytes:
    pcm = bytearray()
    for x in samples_f32:
        x = max(-1.0, min(1.0, float(x)))
        i = int(x * 32767.0)
        pcm += int(i).to_bytes(2, "little", signed=True)
    buf = io.BytesIO()
    with wave.open(buf, "wb") as wf:
        wf.setnchannels(1)
        wf.setsampwidth(2)
        wf.setframerate(sr)
        wf.writeframes(bytes(pcm))
    return buf.getvalue()

def make_silence_wav(ms: int = 80) -> bytes:
    sr = 22050
    n = int(sr * (ms / 1000.0))
    return _to_wav_bytes(sr, [0.0] * n)

def make_faah_synth_wav() -> bytes:
    # Short comedic down-sweep + noise burst
    sr, dur = 22050, 0.26
    n = int(sr * dur)
    out = []
    f0, f1 = 760.0, 150.0
    phase = 0.0
    for i in range(n):
        t = i / sr
        freq = f0 * ((f1 / f0) ** (t / dur))
        phase += (2.0 * math.pi * freq) / sr
        sweep = math.sin(phase)
        noise = (random.random() * 2.0 - 1.0) * 0.09
        env = math.exp(-8.5 * t)
        out.append(math.tanh(2.2 * (0.92 * sweep + noise)) * env)
    return _to_wav_bytes(sr, out)

def make_celebrate_ding_wav() -> bytes:
    # A tiny "ding" chord
    sr, dur = 22050, 0.22
    n = int(sr * dur)
    out = []
    freqs = [880.0, 1108.7, 1320.0]
    for i in range(n):
        t = i / sr
        env = math.exp(-10.0 * t)
        s = 0.0
        for f in freqs:
            s += math.sin(2.0 * math.pi * f * t)
        s = (s / len(freqs)) * env
        out.append(math.tanh(1.8 * s))
    return _to_wav_bytes(sr, out)

