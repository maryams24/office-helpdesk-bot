def extract_action_items(messages: list[dict]) -> list[str]:
    # Lightweight heuristic extractor from recent user messages
    recent = [m["content"] for m in messages[-20:] if m.get("role") == "user"]
    text = "\n".join(recent).lower()
    candidates = []

    # Patterns like: "please X", "need to X", "can you X", "action: X"
    patt_list = [
        r"(?:please|plz|pls)\s+([^\n\.]{6,120})",
        r"(?:need to|we need to|i need to)\s+([^\n\.]{6,120})",
        r"(?:can you|could you)\s+([^\n\?]{6,120})",
        r"(?:action item|action items|next step|next steps)\s*[:\-]\s*([^\n]{6,160})",
    ]
    for p in patt_list:
        for m in re.finditer(p, text):
            s = m.group(1).strip()
            s = re.sub(r"\s+", " ", s)
            s = s.rstrip(" .;:")
            if len(s) >= 6:
                candidates.append(s)

    # De-dupe while preserving order
    out = []
    seen = set()
    for c in candidates:
        key = c[:80]
        if key not in seen:
            seen.add(key)
            out.append(c)
    return out[:12]

def recap_messages(messages: list[dict]) -> str:
    # Simple recap using the last few user prompts + detected actions
    recent_user = [m["content"].strip() for m in messages[-12:] if m.get("role") == "user"]
    recent_user = [x for x in recent_user if x]
    actions = extract_action_items(messages)
    lines = []
    lines.append("Recap (latest):")
    if recent_user:
        lines.append("Context:")
        for x in recent_user[-5:]:
            x1 = re.sub(r"\s+", " ", x)
            lines.append(f"- {x1[:160]}")
    if actions:
        lines.append("\nLikely action items:")
        for a in actions[:8]:
            lines.append(f"- {a}")
    lines.append("\nIf you paste notes, I can format minutes/RAID/status.")
    return "\n".join(lines)

